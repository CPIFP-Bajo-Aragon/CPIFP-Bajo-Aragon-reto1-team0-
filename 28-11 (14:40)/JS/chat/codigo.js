// Función para abrir la ventana modal de empresas
function abrirModalEmpresas() {
    // Obtener el elemento de la ventana modal de empresas y establecer su estilo para mostrarlo
    document.getElementById('myModal').style.display = 'block';
}

// Función para cerrar la ventana modal de empresas
function cerrarModalEmpresas() {
    // Obtener el elemento de la ventana modal de empresas y establecer su estilo para ocultarlo
    document.getElementById('myModal').style.display = 'none';
}


